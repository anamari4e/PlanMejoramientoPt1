public class Variables {
    public static void main(String[] args) {
        int edad= 19;//numero entero
        System.out.println("Mi edad es: "+edad);
        
        float altura= 1.70f; //flotantes o decimales cortos
        System.out.println("Mi altura es: "+altura);
        
        double valorPi= 3.141597465; //decimales mas grandes
        System.out.println("El valor del numero pi es: "+valorPi);
        
        long distanciaSol= 7896542165232265L; //enteros mas grandes y se pone la L al final para especificar.
        System.out.println("Distancia al sol es: "+distanciaSol);
        
        boolean dia= false; 
        System.out.println("Ed de dia: "+dia);
        
        char miInicial= 'A'; //para una sola letra
        System.out.println("La inicial de mi nombre es: "+miInicial);
        
        //La denotacion est√° en camelCase
    }
}
