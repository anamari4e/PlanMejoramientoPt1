public class OperacionesVariables {
    public static void main(String[] args) {
        //rectangulo
        int b=15;
        int a=8;
        
        //area
        
        int rArea= b*a;
        System.out.println("El area del rectangulo es: "+rArea);
        //perimetro
        int rPerimetro= b + a + b + a;
        System.out.println("El perimetro del rectangulo es: "+rPerimetro);
        
        //triangulo
        b=5;
        a=3;
        
        //area
        double tArea= b*a/2;
        System.out.println("El area es: "+tArea);
                
    }
}
